                                            <h2>GDPR Policy</h2>
                                            <p><strong>KOA Services</strong> is committed to complying with the General Data Protection Regulation (GDPR) and protecting your personal data rights. This policy outlines our GDPR compliance practices for all European Union and European Economic Area residents.</p>

                                            <h4>1. Our GDPR Commitment</h4>
                                            <p>We recognize that data protection is a fundamental right. KOA Services processes personal data transparently, lawfully, and only for specified purposes. We comply with all requirements of the GDPR (EU 2016/679).</p>

                                            <h4>2. Legal Basis for Processing</h4>
                                            <p>We process your personal data based on the following legal grounds:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li><strong>Contract Fulfillment:</strong> To provide cleaning services and process bookings</li>
                                                <li><strong>Legitimate Interest:</strong> To improve services, communicate updates, and prevent fraud</li>
                                                <li><strong>Consent:</strong> For marketing communications (with your explicit opt-in)</li>
                                                <li><strong>Legal Obligation:</strong> To comply with tax and employment laws</li>
                                            </ul>

                                            <h4>3. Your GDPR Rights</h4>
                                            <p>Under the GDPR, you have the following rights regarding your personal data:</p>
                                            
                                            <p><strong>Right of Access:</strong> You have the right to request and obtain a copy of all personal data we hold about you in a structured, commonly-used, machine-readable format.</p>

                                            <p><strong>Right to Rectification:</strong> You can request corrections to inaccurate or incomplete personal data. We will update your information promptly.</p>

                                            <p><strong>Right to Erasure ("Right to be Forgotten"):</strong> You can request deletion of your personal data, except where we have legal obligations to retain it or if it's necessary for our legitimate interests.</p>

                                            <p><strong>Right to Restrict Processing:</strong> You may request that we limit the processing of your data in certain circumstances, such as while accuracy is being verified.</p>

                                            <p><strong>Right to Data Portability:</strong> You have the right to receive your personal data in a portable, machine-readable format and transmit it to another organization.</p>

                                            <p><strong>Right to Object:</strong> You can object to processing of your data for direct marketing, profiling, and other purposes at any time.</p>

                                            <p><strong>Rights Related to Automated Decision Making:</strong> You have the right not to be subject to decisions based solely on automated processing, including profiling.</p>

                                            <h4>4. Data Protection Officer</h4>
                                            <p>We have appointed a Data Protection Officer (DPO) to ensure compliance with GDPR requirements. For data protection inquiries, contact:</p>
                                            <p><strong>Data Protection Officer<br>
                                            Email:</strong> <a href="mailto:dpo@koaservices.com">dpo@koaservices.com</a><br>
                                            <strong>Response Time:</strong> 30 days maximum</p>

                                            <h4>5. Personal Data We Collect</h4>
                                            <p>We collect the following categories of personal data:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li>Identification Data (name, email, phone number, address)</li>
                                                <li>Booking Data (service preferences, dates, times, property details)</li>
                                                <li>Payment Data (processed securely through third-party providers)</li>
                                                <li>Communication Data (correspondence related to services)</li>
                                                <li>Technical Data (IP address, browser type, device information)</li>
                                            </ul>

                                            <h4>6. Data Retention Policy</h4>
                                            <p>We retain personal data only as long as necessary for the purposes stated:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li><strong>Booking Data:</strong> 3 years (for accounting and service records)</li>
                                                <li><strong>Customer Communications:</strong> 1 year after last contact</li>
                                                <li><strong>Payment Information:</strong> 7 years (tax compliance)</li>
                                                <li><strong>Website Analytics:</strong> 26 months</li>
                                            </ul>

                                            <h4>7. International Data Transfers</h4>
                                            <p>If we transfer personal data outside the EU/EEA, we ensure appropriate safeguards are in place, such as:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li>Standard Contractual Clauses (SCCs)</li>
                                                <li>EU adequacy decisions</li>
                                                <li>Your explicit consent for transfers</li>
                                            </ul>

                                            <h4>8. Data Subject Rights Requests</h4>
                                            <p>To exercise any of your GDPR rights, you can:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li>Send a written request to our DPO at <a href="mailto:dpo@koaservices.com">dpo@koaservices.com</a></li>
                                                <li>Include identification proof and specify your request clearly</li>
                                            </ul>
                                            <p><strong>We will respond within 30 calendar days.</strong> If your request is complex, we may extend by 60 additional days with notification.</p>

                                            <h4>9. Data Breach Notification</h4>
                                            <p>In the event of a personal data breach, we will:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li>Notify you within 72 hours if there is high risk to your rights</li>
                                                <li>Inform relevant supervisory authorities</li>
                                                <li>Take appropriate remedial measures</li>
                                                <li>Document the incident thoroughly</li>
                                            </ul>

                                            <h4>10. Data Security</h4>
                                            <p>We implement appropriate technical and organizational measures to protect your personal data against unauthorized processing, accidental loss, destruction, or damage. Our security measures include encryption, secure servers, and restricted access to personal data.</p>

                                            <h4>11. Children's Data Protection</h4>
                                            <p>Our services are not intended for children under 16 years old. If we learn that we have collected data from a child under 16 without parental consent, we will immediately delete it.</p>

                                            <h4>12. Cookies & Tracking Under GDPR</h4>
                                            <p>We obtain explicit consent before placing non-essential cookies on your device. You can manage cookie preferences at any time through your browser settings or our cookie preferences center.</p>

                                            <h4>13. Third-Party Data Processors</h4>
                                            <p>We have Data Processing Agreements (DPAs) in place with all third-party service providers who handle your personal data, ensuring GDPR compliance at every step.</p>

                                            <h4>14. Supervisory Authority</h4>
                                            <p>If you believe we have violated your GDPR rights, you have the right to lodge a complaint with your local supervisory authority:</p>
                                            <ul style="color: #333; margin-left: 20px;">
                                                <li>Find your local authority at: <a href="https://edpb.ec.europa.eu/about-edpb/board/members_en" target="_blank">https://edpb.ec.europa.eu</a></li>
                                                <li>You can also contact your country's data protection authority</li>
                                            </ul>

                                            <h4>15. Changes to This Policy</h4>
                                            <p>We will update this GDPR Policy as needed to reflect legal changes or improvements to our practices. Updates will be posted on this page with a new "Last Updated" date.</p>

                                            <h4>16. Contact Information</h4>
                                            <p><strong>KOA Services GDPR Compliance Team</strong><br>
                                            Email: <a href="mailto:dpo@koaservices.com">dpo@koaservices.com</a><br>
                                            Phone: +1-800-CLEAN-NOW<br>
                                            Address: 123 Clean Street, Service City, SC 12345<br>
                                            Website: www.koaservices.com</p>

                                            <p><strong>Last Updated: October 2025</strong></p>
