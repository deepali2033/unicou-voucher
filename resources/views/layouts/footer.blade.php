<script>
    document.addEventListener('DOMContentLoaded', function () {
        const subscriptionForm = document.getElementById('newsletter-subscription-form');
        const emailInput = document.getElementById('form-field-email_newsletter');
        const submitBtn = document.getElementById('subscribe-btn');

        if (subscriptionForm) {
            // Remove any Elementor event listeners
            const newForm = subscriptionForm.cloneNode(true);
            subscriptionForm.parentNode.replaceChild(newForm, subscriptionForm);

            // Re-get references after cloning
            const form = document.getElementById('newsletter-subscription-form');
            const input = document.getElementById('form-field-email_newsletter');
            const btn = document.getElementById('subscribe-btn');

            form.addEventListener('submit', function (e) {
                e.preventDefault();
                e.stopPropagation();
                e.stopImmediatePropagation();

                // Disable submit button to prevent double submission
                const originalBtnText = btn.querySelector('.elementor-button-text').textContent;
                btn.disabled = true;
                btn.querySelector('.elementor-button-text').textContent = 'Processing...';

                // Get form data
                const formData = new FormData(form);

                // Send AJAX request
                fetch('{{ route("subscription.store") }}', {
                    method: 'POST',
                    headers: {
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]')?.content || document.querySelector('input[name="_token"]').value,
                        'Accept': 'application/json',
                    },
                    body: formData
                })
                    .then(response => response.json())
                    .then(data => {
                        if (data.success) {
                            // Show success state on button
                            btn.querySelector('.elementor-button-text').textContent = 'Subscribed!';
                            btn.classList.add('btn-success');

                            // Show success message
                            showSubscriptionMessage(data.message, 'success');
                            // Clear email input
                            input.value = '';

                            // Reset button after 2 seconds
                            setTimeout(() => {
                                btn.querySelector('.elementor-button-text').textContent = 'Subscribe';
                                btn.classList.remove('btn-success');
                                btn.disabled = false;
                            }, 2000);
                        } else {
                            // Show error message
                            showSubscriptionMessage(data.message, 'error');
                            // Reset button immediately on error
                            btn.disabled = false;
                            btn.querySelector('.elementor-button-text').textContent = 'Subscribe';
                        }
                    })
                    .catch(error => {
                        console.error('Error:', error);
                        showSubscriptionMessage('An error occurred. Please try again later.', 'error');
                        // Reset button immediately on error
                        btn.disabled = false;
                        btn.querySelector('.elementor-button-text').textContent = 'Subscribe';
                    });
            });
        }

        function showSubscriptionMessage(message, type) {
            // Create modal/popup for the message
            const modalHTML = `
            <div class="modal fade" id="subscriptionModal" tabindex="-1" aria-labelledby="subscriptionModalLabel" aria-hidden="true">
                <div class="modal-dialog modal-dialog-centered">
                    <div class="modal-content">
                        <div class="modal-header ${type === 'success' ? 'bg-success' : 'bg-danger'} text-white">
                            <h5 class="modal-title" id="subscriptionModalLabel">
                                ${type === 'success' ? '✓ Success' : '✗ Error'}
                            </h5>
                            <button type="button" class="btn-close btn-close-white" data-bs-dismiss="modal" aria-label="Close"></button>
                        </div>
                        <div class="modal-body">
                            <p class="mb-0">${message}</p>
                        </div>
                        <div class="modal-footer">
                            <button type="button" class="btn ${type === 'success' ? 'btn-success' : 'btn-danger'}" data-bs-dismiss="modal">Close</button>
                        </div>
                    </div>
                </div>
            </div>
        `;

            // Remove existing modal if present
            const existingModal = document.getElementById('subscriptionModal');
            if (existingModal) {
                existingModal.remove();
            }

            // Add modal to body
            document.body.insertAdjacentHTML('beforeend', modalHTML);

            // Show modal
            const modal = new bootstrap.Modal(document.getElementById('subscriptionModal'));
            modal.show();

            // Remove modal from DOM after it's hidden
            document.getElementById('subscriptionModal').addEventListener('hidden.bs.modal', function () {
                this.remove();
            });
        }
    });
</script>
